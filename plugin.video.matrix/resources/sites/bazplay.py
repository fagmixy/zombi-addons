def __checkForNextPage(sHtmlContent):
    sPattern = '<link rel="next" href="([^"]+)'
	
    oParser = cParser()
    aResult = oParser.parse(sHtmlContent, sPattern)
 
    if aResult[0]:
        
        return aResult[1][0]
    else:
        sPattern = "<span class='current'>.+?<a href='([^']+)"
        oParser = cParser()
        aResult = oParser.parse(sHtmlContent, sPattern)
 
        if aResult[0]:
        
            return aResult[1][0]

    return False 

